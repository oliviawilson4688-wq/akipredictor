FK506_MAP = {
    "持续达标":   0.0,
    "间歇性达标": 0.40498607000779124,
    "从未达标":   1.2916688217943653,
}


def sigmoid(x):
    return 1.0 / (1.0 + math.exp(-x))

def safe_logit(p):
    p = max(1e-9, min(1 - 1e-9, p))
    return math.log(p / (1.0 - p))

def predict(inputs):
    lp = INTERCEPT
    for key, params in NUMERIC_PARAMS.items():
        raw = inputs.get(key)
        if raw is None:
            raw = params["impute"]
        z = (raw - params["mean"]) / params["sd"]
        lp += params["coef"] * z
    for key, params in BINARY_PARAMS.items():
        lp += params["coef"] * int(inputs.get(key, 0))
    lp += FK506_MAP.get(inputs.get("FK506", "持续达标"), 0.0)
    p_raw = sigmoid(lp)
    p_cal = sigmoid(CAL_A + CAL_B * safe_logit(p_raw))
    return {"lp": round(lp, 5), "p_raw": round(p_raw, 5), "p_cal": round(p_cal, 5)}


